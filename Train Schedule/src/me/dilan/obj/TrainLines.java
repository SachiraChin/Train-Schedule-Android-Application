package me.dilan.obj;

public class TrainLines implements Objects {
	
	private  int[] ids;
	private  String[] names;
	private int count;
	
	
	public int[] getIds() {
		return ids;
	}

	public void setIds(int[] ids) {
		this.ids = ids;
	}

	public String[] getNames() {
		return names;
	}

	public void setNames(String[] names) {
		this.names = names;
	}

	public int getCount() {		
		return count;
	}

	public void setCount(int count) {
		this.count = count;
		
	}	

}
